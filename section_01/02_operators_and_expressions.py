# Operators and Expressions
###

# The most common operator is the Assignment operator
# it is represented by a single equals symbol

# the value of expression to the right of the equals operator is calculated,
# then the value is copied into the memory location referenced by the variable name on the left
x = 3
y = 2

# The next most common operator is the  Comparison operator
assert x == 3
assert y == 3

# The next most common operators are simple math operators
z = x + y
assert z == 5

# relational operators
assert 1 == 1  # is equal to
assert 2 > 1   # is greater than
assert 3 >= 3  # is greater than or equal to
assert 4 != 3  # is not equal to
